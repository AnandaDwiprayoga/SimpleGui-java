    private void tampilkanTable(){
        String[] kolom = {"ID Matkul", "Nama Mata Kuliah", "Jumlah Bobot SKS"};
        Object[] obj = new Object[3];
        ArrayList<AmbilMatkul> listMatkul = new AmbilMatkul().getAll();
        
        jTable1.setModel(new DefaultTableModel(new Object[][]{}, kolom));
        
        for(AmbilMatkul matkul : listMatkul){
            obj[0] = matkul.getIdMatkul();
            obj[1] = matkul.getNamaMatkul();
            obj[2] = matkul.getJumlahSks();
            
            ((DefaultTableModel) jTable1.getModel()).addRow(obj);
        }
        
    }

