    private void setDropDownSemster(){
        String[] smtr =  {"Pilih Semester","Semester 1","Semester 2","Semester 3","Semester 4","Semester 5","Semester 6","Semester 7","Semester 8"};
        ddSemester = smtr;
        cbSemester.setModel(new DefaultComboBoxModel(ddSemester));
    }
    
    private void setDropDownMatkul(){
        ArrayList<Object> listMatkul = new ArrayList();
        listMatkul.add("Pilih Mata Kuliah");
        idMatkul.add(-1);
        
        for(AmbilMatkul matkul : new AmbilMatkul().getAll()){
            listMatkul.add(matkul.getNamaMatkul());
            idMatkul.add(matkul.getIdMatkul());
        }
        cbMatkul.setModel(new DefaultComboBoxModel(listMatkul.toArray()));
    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(cbMatkul.getSelectedIndex() == 0 && cbSemester.getSelectedIndex() == 0){
            labelValidasi.setText("Pilih semester dan matkul terlebih dahulu");
            return;
        }
        else if(cbSemester.getSelectedIndex() == 0){
            labelValidasi.setText("Pilih semester terlebih dahulu");
            return;
        }else if(cbMatkul.getSelectedIndex() == 0){
            labelValidasi.setText("Pilih matkul terlebih dahulu");
            return;
        }
        
        labelValidasi.setText("");
        AmbilMatkul ambilMatkul = new AmbilMatkul();
        ambilMatkul.setSemester(cbSemester.getSelectedIndex());
        ambilMatkul.setIdMatkul(idMatkul.get(cbMatkul.getSelectedIndex()));
        ambilMatkul.setUser(currentUser);
        ambilMatkul.insertKRS();
        tampilTable(new AmbilMatkul(cbSemester.getSelectedIndex(), currentUser).getMatkulByUsrSmtr());
    }//GEN-LAST:event_jButton1ActionPerformed

