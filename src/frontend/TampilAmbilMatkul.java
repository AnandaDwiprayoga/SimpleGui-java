    private void btnSearchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSearchMouseClicked
        // TODO add your handling code here:
        for(AmbilMatkul matkul : new AmbilMatkul().getAll()){
            if (searchMatkul.getText().equalsIgnoreCase(matkul.getNamaMatkul())) {
                labelError.setText("");
                cbMatkul.setSelectedItem(matkul.getNamaMatkul());
                return;
            }
        }
        String paragraph = searchMatkul.getText() + " Tidak ditemukan";
        labelError.setText("<html><p style=\"width:180px\">"+paragraph+"</p></html>");
        cbMatkul.setSelectedIndex(0);
    }//GEN-LAST:event_btnSearchMouseClicked

    private void cbSemesterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbSemesterActionPerformed
        // TODO add your handling code here:
        for(int i = 0; i < ddSemester.length; i++){
            if (cbSemester.getSelectedItem().toString().equals(ddSemester[i]) ) {
                AmbilMatkul ambilMatkul = new AmbilMatkul(i, currentUser);
                tampilTable(ambilMatkul.getMatkulByUsrSmtr());
            }
        }
    }//GEN-LAST:event_cbSemesterActionPerformed
    
    private void tampilTable(ArrayList<AmbilMatkul> list){
        String[] kolom = {"ID SKS", "Nama Mata Kuliah","Bobot SKS"};
        Object[] object = new Object[3];
        
        jTable1.setModel(new DefaultTableModel(new Object[][]{}, kolom));
        
        for(AmbilMatkul am : list){
            object[0] = am.getId();
            object[1] = am.getNamaMatkul();
            object[2] = am.getJumlahSks();
            
            ((DefaultTableModel) jTable1.getModel()).addRow(object);
        }
    }
