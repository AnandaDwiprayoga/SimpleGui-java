    private void inputNIMKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_inputNIMKeyTyped
        // TODO add your handling code here:
        if(evt.getKeyChar() >= '0' && evt.getKeyChar() <= '9' || evt.getKeyChar() == KeyEvent.VK_DELETE || evt.getKeyChar()== KeyEvent.VK_BACK_SPACE ){
            inputNIM.setEditable(true);
        }else{
            inputNIM.setEditable(false);
        }
    }//GEN-LAST:event_inputNIMKeyTyped

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (inputNIM.getText().isEmpty() && inputPassword.getText().isEmpty()){
            labelNotice.setText("Masukkan nim dan password");
            return;
        }else if(inputNIM.getText().isEmpty()){
            labelNotice.setText("Masukkan nim anda");
            return;
        }else if(inputPassword.getText().isEmpty()){
            labelNotice.setText("Masukkan password anda");
            return;
        }
        labelNotice.setText("");
        User user = new User(Integer.parseInt(inputNIM.getText()), inputPassword.getText());
        if (user.login()) {
            this.setVisible(false);
            Home home = new Home();
            home.setHome(user.getUserLogin());
            home.setLocationRelativeTo(null);
            home.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(this, "Akun anda tidak ditemukan");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

